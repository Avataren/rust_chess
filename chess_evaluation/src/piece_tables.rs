const PAWN_TABLE: [i32; 64] = [
    0, 0, 0, 0, 0, 0, 0, 0, 50, 50, 50, 50, 50, 50, 50, 50, 10, 10, 20, 30, 30, 20, 10, 10, 5, 5,
    10, 25, 25, 10, 5, 5, 0, 0, 0, 20, 20, 0, 0, 0, 5, -5, -10, 0, 0, -10, -5, 5, 5, 10, 10, -20,
    -20, 10, 10, 5, 0, 0, 0, 0, 0, 0, 0, 0,
];

const KNIGHT_TABLE: [i32; 64] = [
    -50, -40, -30, -30, -30, -30, -40, -50, -40, -20, 0, 0, 0, 0, -20, -40, -30, 0, 10, 15, 15, 10,
    0, -30, -30, 5, 15, 20, 20, 15, 5, -30, -30, 0, 15, 20, 20, 15, 0, -30, -30, 5, 10, 15, 15, 10,
    5, -30, -40, -20, 0, 5, 5, 0, -20, -40, -50, -40, -30, -30, -30, -30, -40, -50,
];

pub fn evaluate_pawn_position(square: usize, is_white: bool) -> i32 {
    if is_white {
        PAWN_TABLE[square] // Directly use the index for white
    } else {
        PAWN_TABLE[63 - square] // Flip index for black
    }
}

pub fn evaluate_knight_position(square: usize, is_white: bool) -> i32 {
    if is_white {
        KNIGHT_TABLE[square] // Directly use the index for white
    } else {
        KNIGHT_TABLE[63 - square] // Flip index for black
    }
}
